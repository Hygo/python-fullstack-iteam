
"""
Programa para exibição de ficha cadastral básica de funcionário.

Este script define variáveis com informações pessoais, financeiras e de 
status, exibindo todas em uma única linha no console.

"""

nome = "Ana Lima"      # Armazena o nome do funcionário
idade = 29             # Armazena a idade em anos (inteiro)
salario = 4500.00      # Armazena o salário mensal (float)
ativo = True           # Indica se o funcionário está ativo (booleano)
